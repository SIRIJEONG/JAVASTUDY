public class test12 {
    public static void main(String[] args) {
        //단항 연산자

        int i = 3;
        i++; //i+1이라는 뜻
        System.out.println(i); //4
        ++i;
        System.out.println(i); //5
        System.out.println(++i); //6
        System.out.println(i++); //6
        System.out.println(i); //7
    }
}
